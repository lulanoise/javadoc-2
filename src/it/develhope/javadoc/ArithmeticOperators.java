package it.develhope.javadoc;

/**
 * The class Arithmetic operators.
 */
public class ArithmeticOperators {

    /**
     * The sum method
     * @param num1 the num 1
     * @param num2 the num 2
     * @return the int summation
     */
    public int sum(int num1, int num2){
        return num1 + num2;
    }

    /**
     * The Sub method
     * @param num1 the num 1
     * @param num2 the num 2
     * @return the int subtraction
     */
    public int sub(int num1, int num2){
        return num1 - num2;
    }

    /**
     * The Mul method
     * @param num1 the num 1
     * @param num2 the num 2
     * @return the int multiplication
     */
    public int mul(int num1, int num2){
        return num1 * num2;
    }


    /**
     * The Div method
     * @param num1 the num 1
     * @param num2 the num 2
     * @return the int division
     */

    public int div(int num1, int num2){
        return num1/num2;
    }
   
}
